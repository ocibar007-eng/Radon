import { build } from 'esbuild';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const entry = path.join(repoRoot, 'src', 'server', 'pipeline-bundle-entry.ts');
const outFile = path.join(repoRoot, 'api', 'pipeline', 'bundle.cjs');

fs.mkdirSync(path.dirname(outFile), { recursive: true });

await build({
  entryPoints: [entry],
  outfile: outFile,
  bundle: true,
  platform: 'node',
  format: 'cjs',
  target: 'node22',
  sourcemap: false,
  logLevel: 'info',
  banner: {
    js: '/* Generated by scripts/build_pipeline_bundle.mjs */',
  },
});
